    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
      
        Producto p = new Producto();
        Fcompra fp = new Fcompra();
        for (int i = 0; i < this.tblCom.getRowCount(); i++) {
            p.setIdProducto(Integer.parseInt(this.tblCom.getValueAt(i, 0).toString()));
            p.setCantidadProdu(Integer.parseInt(this.tblCom.getValueAt(i, 3).toString()));
            p.setFechaCompra(this.cbFechaReg.getDate());
            p.setFechaVenci(this.cbFechaVenci.getDate());
        if (fp.actualizar(p)!=0) {
            
            JOptionPane.showMessageDialog(this, "Compra realizada");
            
        }else{
            JOptionPane.showMessageDialog(this, "No se pudo realizar la compra", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
        
        }
      
        
        
    }//GEN-LAST:event_btnComprarActionPerformed

    private void btnNuevaCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevaCompraActionPerformed
      
        Producto p = new Producto();
        Fcompra fp = new Fcompra();
        
        if (this.txtIdSupli.getText().isEmpty() || this.cbFechaVenci.getDate().toString().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Campos en blanco", "Mensaje", JOptionPane.ERROR_MESSAGE);
            
        }else{
            for (int i = 0; i < this.tblCom.getRowCount() ; i++) {
            p.setIdProducto(Integer.parseInt(this.tblCom.getValueAt(i, 0).toString()));
            p.setNombrePro(this.tblCom.getValueAt(i, 1).toString());
            p.setDescripcionPro(this.tblCom.getValueAt(i, 2).toString());
            p.setCantidadProdu(Integer.parseInt(this.tblCom.getValueAt(i, 3).toString()));
            p.setFechaCompra(this.cbFechaReg.getDate());
            p.setFechaVenci(this.cbFechaVenci.getDate());
            p.setNumSupli(Integer.parseInt(this.txtIdSupli.getText()));
            p.setNumCatego(Integer.parseInt(this.tblCom.getValueAt(i, 7).toString()));
            p.setPrecioVenta(Double.parseDouble(this.tblCom.getValueAt(i, 5).toString()));
            p.setPrecioCompra(Double.parseDouble(this.tblCom.getValueAt(i, 4).toString()));
            if (fp.insertarPro(p)) {
                JOptionPane.showMessageDialog(this, "Compra nueva realizada");
                
            }else{
                JOptionPane.showMessageDialog(this, "No se puede realizar la compra", "Mensaje", JOptionPane.ERROR_MESSAGE);
            }
            
        }
        }
        
        
            
        
       
        
    }//GEN-LAST:event_btnNuevaCompraActionPerformed

