    void CalcularImport(){
       double a= Double.parseDouble(this.txtPrecioCom.getText());
       int b = Integer.parseInt(this.txtCantidad.getText());
       
       double resultado = a*b;
       this.txtImport.setText(""+resultado);
    }
    
    void sumarMonto(){
        double suma1 = 0;
        for (int i = 0; i < this.tblCom.getRowCount(); i++) {
            double suma = Double.parseDouble( this.tblCom.getValueAt(i, 8).toString());
            suma1 +=suma;
                    
            
        }
        this.txtMonto.setText(""+suma1);
    }
    
